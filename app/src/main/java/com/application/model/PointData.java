public class PointData {

    private String first_menu;
    private String amount;
    private String recode_point;
    private String order_date;

    public String getFirst_menu() {
        return first_menu;
    }

    public void setFirst_menu(String first_menu) {
        this.first_menu = first_menu;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getRecode_point() {
        return recode_point;
    }

    public void setRecode_point(String recode_point) {
        this.recode_point = recode_point;
    }

    public String getOrder_date() {
        return order_date;
    }

    public void setOrder_date(String order_date) {
        this.order_date = order_date;
    }
}
